/**
 * Dividing a by b
 * @param {number} a - dividend
 * @param {number} b - divisor
 * @returns {number} quotient
 * 
 * 
 * const a = 10;
 * const b = 5;
 * 
 * const result = divide(a, b);
 * console.log(result); // 2
 * 
 * @throws {ZeroDivisionError} when b is 0
 * 
 * @author wuja
 * 
 */

function divide(a, b) {
  return a / b;
}